package com.example.user.proyec.entidades;

public class Datos {
    private String temp;
    private String pos;
    private String dor;
    private String ppm2;

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getPos() {
        return pos;
    }

    public void setPos(String pos) {
        this.pos = pos;
    }

    public String getDor() {
        return dor;
    }

    public void setDor(String dor) {
        this.dor = dor;
    }

    public String getPpm2() {
        return ppm2;
    }

    public void setPpm2(String ppm2) {
        this.ppm2 = ppm2;
    }
}
